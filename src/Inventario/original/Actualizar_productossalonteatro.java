    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        try {
           Conexion Con = new Conexion();
           Con.ConectarBD();
         String idt = id.getText();  
        String nombre = nombret.getText();
        String referencia = referenciat.getText();
        String descripcion = descripciont.getText();
        String tipo=tipot.getText();
        
           String SQL = "UPDATE producto_salonteatro SET "
                    + "id_producto_t='"+idt+"',nombre_ot='"+nombre+"',referencia_ot='"+referencia+"',descripcion_t='"+descripcion+"',tipo_t='"+tipo+"'"
                   +"WHERE  id_producto_t='"+idt+"'";
           Con.sentencia.execute(SQL);
           JOptionPane.showMessageDialog(null,
                   "Â¡Registro Actualizado con Exito!");
           dispose();
           Con.DesconectarBD();
       }catch (SQLException ex) {
        Logger.getLogger(Actualizar_productossalonteatro.class.getName()).log(Level.SEVERE, null, ex);
        }

         setVisible(false);
        Inventario_salonteatro q =new Inventario_salonteatro();
         
       
        q.setVisible (true);
         q.pninventario.setVisible(false);
    q.pnproductos.setVisible(true);
     q.pnentradas.setVisible(false);
      q.pnsalidas.setVisible(false);
    }//GEN-LAST:event_btnactualizarActionPerformed

