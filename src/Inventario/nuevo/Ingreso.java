    private void cmdAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAceptarActionPerformed
        try {
            String nombre = txtNombre.getText();
            String referencia = txtReferencia.getText();
            String descripcion = txtDescripcion.getText();
            String tipo = txtTipo.getText();
            int cantidad = Integer.parseInt(spnCantidad.getModel().getValue().toString());
            if (cantidad > 0) {
                Conexion con = new Conexion();
                con.ConectarBD();
                String SQL = "INSERT INTO "+db+"(nombre,referencia,descripcion,tipo,cantidad) values ('"+nombre+"','"+referencia+"','"+descripcion+"','"+tipo+"',"+cantidad+");";
                con.sentencia.executeUpdate(SQL);
                con.DesconectarBD();
                metodos.registrarMovimiento("Registro", String.valueOf(cantidad),this.userID);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Cantidad no puede ser cero");
            }
            
        } catch (SQLException ex){
            Logger.getLogger(Inventario_salonteatro.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }//GEN-LAST:event_cmdAceptarActionPerformed

