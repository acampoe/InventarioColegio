    private int guardarUsuario(String num_doc, String nombre, String correo, String contrasena) {
        try {
            Conexion con = new Conexion();
            con.ConectarBD();
            String SQL = "INSERT INTO login(num_doc,nombre,correo,contraseña) VALUES ('" + num_doc + "','" + nombre + "','" + correo + "','" + contrasena + "');";
            int resultado = con.sentencia.executeUpdate(SQL);
            return resultado;
        } catch (SQLException ex) {
            return 0;
        }
    }

