    private void cmdAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAceptarActionPerformed
        // TODO add your handling code here:
        String num_doc, nombre, correo, pass1, pass2;
        num_doc = txtNumDoc.getText();
        nombre = txtNombre.getText();
        correo = txtEmail.getText();
        pass1 = txtPass1.getText();
        pass2 = txtPass2.getText();
        if (num_doc.isEmpty() || nombre.isEmpty() || correo.isEmpty() || pass1.isEmpty() || pass2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios");
        } else if (pass1.equals(pass2)) {
            if (validarId(num_doc)) {
                if (validarCorreo(correo)) {
                    guardarUsuario(num_doc, nombre, correo, pass1);
                    JOptionPane.showMessageDialog(this, "Registro Exitoso");
                    this.dispose();
                }else {
                    JOptionPane.showMessageDialog(this, "El correo ya se encuentra registrado");
                }
            } else {
                JOptionPane.showMessageDialog(this, "1. ID ya existe");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Verifique la contraseña");
        }
    }//GEN-LAST:event_cmdAceptarActionPerformed

